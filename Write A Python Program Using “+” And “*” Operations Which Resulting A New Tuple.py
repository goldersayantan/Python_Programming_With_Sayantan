# Write A Python Program Using “+” And “*” Operations Which Resulting A New Tuple...
tup1 = (10, 20, 30, 40, 50, 60, 70, 80, 90)
tup2 = (100, 200, 300, 400, 500, 600, 700, 800, 900)
print("The new tuple after using + will be: ", tup1 + tup2)
print("The new tuple after using * will be: ", tup1 * 2)
print("The new tuple after using * will be: ", tup2 * 2)
